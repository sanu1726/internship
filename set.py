# Set with initial values
fruits = {'apple', 'banana', 'cherry'}
print(fruits)

fruits.add ('Mango') # update the set
print(fruits)

fruits.remove ('cherry') #remove items from the set
print(fruits)

fruits.discard('apple') # discard the element from the set 
print(fruits)

fruits.add('pine apple') # add element of the list 
print(fruits)
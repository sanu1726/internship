# List with elements
fruits = ['apple', 'banana', 'cherry']
print(fruits)
fruits.append('mango') #adding an element in a list 
print(fruits)
fruits.remove('apple') #delete an element in a list
print(fruits)
print(fruits.count('mango')) # counting an element which is present is a list and print the value of count 
fruits[2] = 'apple' # modifying the element of the list
print(fruits)